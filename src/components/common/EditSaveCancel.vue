<template>
  <v-layout class="mb-2">
    <!-- <v-flex>{{isValid}}</v-flex> -->
    <v-spacer></v-spacer>
    <v-flex class="mr-4" xs1>
      <v-btn v-if="isReadOnly" @click="editForm">Edit</v-btn>
      <v-btn v-else @click="saveForm" :disabled="disableSave">Save</v-btn>
    </v-flex>
    <v-flex class="mr-4" xs1>
      <v-btn v-if="isReadOnly" @click="closeForm">Close</v-btn>
      <v-btn v-else @click="cancelForm">Cancel</v-btn>
    </v-flex>
  </v-layout>
</template>

<script>
export default {
  name: "EditSaveCancel",
  props: {
    isReadOnly: Boolean,
    isValid: {
      type: Boolean,
      default: true,
    },
  },
  computed: {
    disableSave() { return !this.isValid; }
  },
  methods: {
    editForm() {
      this.$emit("editForm");
    },
    saveForm() {
      this.$emit("saveForm");
    },
    cancelForm() {
      // console.log( 'EditSaveCancel - cancelForm')
      this.$emit("cancelForm");
    },
    closeForm() {
      this.$emit("closeForm");
    },
  },
};
</script>

